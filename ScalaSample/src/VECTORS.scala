
//The Vector class is an indexed, immutable sequence. 
// The “indexed” part of the description means that you can access
//Vector elements very rapidly by their index value,such as accessing
// listOfPeople(999999)

object VECTORS {
  val nums = Vector(1, 2, 3, 4, 5)

val strings = Vector("one", "two")

  def main(args: Array[String]){
    //Because Vector is immutable, you can’t add new elements to it.
    //Instead you create a new sequence by appending or prepending
    //elements to an existing Vector. For instance, given this Vector
    val b = nums :+ 4
    print(b)
  }
}